void main() {
  int operand1 = 101;
  int operand2 = 2;

  int addition = operand1 + operand2; // Addition
  int subtraction = operand1 - operand2; // Subtraction
  int multiplication = operand1 * operand2; // Multiplication
  double division = operand1 / operand2; // Division (result will be a double)
  int remainder = operand1 % operand2; // Modulo (remainder)

  print("Addition: $addition");
  print("Subtraction: $subtraction");
  print("Multiplication: $multiplication");
  print("Division: $division");
  print("Remainder: $remainder");
}
